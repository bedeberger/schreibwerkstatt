#!/usr/bin/env node
// Regenerates db/squashed-schema.js from a fresh migration run.
// Usage: node tools/regen-squashed.js

const fs = require('fs');
const path = require('path');
const { execFileSync } = require('child_process');

const root = path.resolve(__dirname, '..');
const dumper = path.join(root, 'tools', 'dump-schema.js');
const target = path.join(root, 'db', 'squashed-schema.js');

const sql = execFileSync('node', [dumper], { encoding: 'utf8', maxBuffer: 16 * 1024 * 1024 });

const out = `// Auto-generated by tools/regen-squashed.js — DO NOT EDIT BY HAND.
//
// Final database schema at SQUASHED_VERSION, produced by running every legacy
// migration on a fresh DB and dumping sqlite_master in canonical form. Used by
// db/migrations.js to fast-path fresh installs without walking 100+ ALTER/CREATE
// statements. Drift between this snapshot and the legacy migration chain is
// gated by tests/unit/squash-drift.test.mjs — regenerate via:
//   node tools/regen-squashed.js

const SQUASHED_VERSION = ${detectVersion(sql)};

const SQUASHED_SCHEMA = ${JSON.stringify(sql)};

module.exports = { SQUASHED_VERSION, SQUASHED_SCHEMA };
`;

fs.writeFileSync(target, out);
console.log(`Wrote ${path.relative(root, target)} (SQUASHED_VERSION = ${detectVersion(sql)})`);

function detectVersion(text) {
  const m = text.match(/INSERT INTO schema_version\(version\) VALUES \((\d+)\)/);
  if (!m) throw new Error('Could not detect schema_version in dump output');
  return Number(m[1]);
}
